                                    stmt.setString(4, ParentNumber.getText());
                                    stmt.setString(5, ChildAddress.getText());
                                    stmt.setString(6, ChildGender.getSelectedItem().toString());
                                    stmt.setString(8, ChildAge.getSelectedItem().toString());
                                    stmt.setString(7, ParentName.getText());
